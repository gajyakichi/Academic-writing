#!/bin/bash

# 日本語論文（main_J.tex）のコンパイルスクリプト

set -e

echo "========================================="
echo "📝 日本語論文をコンパイル中..."
echo "========================================="

# latexmkを使用してコンパイル（日本語用設定ファイルを指定）
latexmk -r .latexmkrc_jp main_J.tex

echo ""
echo "========================================="
echo "✅ コンパイル完了！"
echo "出力ファイル: main_J.pdf"
echo "========================================="
